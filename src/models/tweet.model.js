import mongoose from "mongoose";

const TweetSchema = new mongoose.Schema(
  {
    owner: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Tweet = mongoose.model("Tweet", TweetSchema);

export default Tweet;
